from ramda.internal import __ as _, _curry2, _curry3
from ramda.shared import eq


def describe_curry2():
    def it_supports_placeholder():
        f = lambda a, b: [a, b]
        g = _curry2(f)

        eq(g(1)(2), [1, 2])
        eq(g(1, 2), [1, 2])

        eq(g(_, 2)(1), [1, 2])
        eq(g(1, _)(2), [1, 2])

        eq(g(_, _)(1)(2), [1, 2])
        eq(g(_, _)(1, 2), [1, 2])
        eq(g(_, _)(_)(1, 2), [1, 2])
        eq(g(_, _)(_, 2)(1), [1, 2])


def describe_curry3():
    def it_supports_placeholder():
        f = lambda a, b, c: [a, b, c]
        g = _curry3(f)

        eq(g(1)(2)(3), [1, 2, 3])
        eq(g(1)(2, 3), [1, 2, 3])
        eq(g(1, 2)(3), [1, 2, 3])

        eq(g(1, 2, 3), [1, 2, 3])

        eq(g(_, 2, 3)(1), [1, 2, 3])
        eq(g(1, _, 3)(2), [1, 2, 3])
        eq(g(1, 2, _)(3), [1, 2, 3])

        eq(g(1, _, _)(2)(3), [1, 2, 3])
        eq(g(_, 2, _)(1)(3), [1, 2, 3])
        eq(g(_, _, 3)(1)(2), [1, 2, 3])

        eq(g(1, _, _)(2, 3), [1, 2, 3])
        eq(g(_, 2, _)(1, 3), [1, 2, 3])
        eq(g(_, _, 3)(1, 2), [1, 2, 3])

        eq(g(1, _, _)(_, 3)(2), [1, 2, 3])
        eq(g(_, 2, _)(_, 3)(1), [1, 2, 3])
        eq(g(_, _, 3)(_, 2)(1), [1, 2, 3])

        eq(g(_, _, _)(_, _)(_)(1, 2, 3), [1, 2, 3])
        eq(g(_, _, _)(1, _, _)(_, _)(2, _)(_)(3), [1, 2, 3])
